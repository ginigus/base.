-- ------------------------------------------------------------
-- 1.1 «Клиенты, которые ни разу ничего не заказывали»
-- ------------------------------------------------------------
-- (а) ОШИБКА:
-- В orders.customer_id есть NULL (гостевые заказы, например заказ 8).
-- Подзапрос SELECT customer_id FROM orders вернёт среди прочего NULL.
-- Для NOT IN (список), если в списке встречается хотя бы один NULL,
-- всё выражение "x NOT IN (...)" становится NULL (UNKNOWN) для КАЖДОЙ
-- строки — и WHERE отбрасывает вообще все строки, независимо от того,
-- заказывал клиент что-то или нет. На наших данных запрос вернёт
-- пустой результат, хотя реально ничего не заказывал только
-- Смирнов Игорь (id=5).
--
-- Было (сломано):
-- SELECT name FROM customers
-- WHERE id NOT IN (SELECT customer_id FROM orders);

-- (б) ИСПРАВЛЕНО (вариант 1 — NOT EXISTS, самый надёжный):
SELECT name FROM customers c
WHERE NOT EXISTS (
    SELECT 1 FROM orders o WHERE o.customer_id = c.id
);

-- (б) ИСПРАВЛЕНО (вариант 2 — если хочется оставить NOT IN,
-- нужно явно выкинуть NULL из подзапроса):
-- SELECT name FROM customers
-- WHERE id NOT IN (SELECT customer_id FROM orders WHERE customer_id IS NOT NULL);


-- ------------------------------------------------------------
-- 1.2 «Количество заказов у каждого клиента»
-- ------------------------------------------------------------
-- (а) ОШИБКА:
-- Джойн с order_items размножает строки заказа по количеству
-- позиций в нём (fan-out): если в заказе 2 товарные строки,
-- он посчитается дважды. COUNT(o.id) в этом случае считает не
-- заказы, а строки после джойна. Например, у Иванова Петра
-- реально 3 заказа, но запрос покажет 4 — потому что в одном
-- из его заказов (заказ №1) две позиции в order_items.
-- Дополнительно: INNER JOIN с orders исключает клиентов без
-- заказов вообще (например, Смирнова Игоря) — если по условию
-- задачи такие клиенты должны быть в отчёте с нулём, это тоже
-- ошибка (см. 1.3, где это явно требуется).
--
-- Было (сломано):
-- SELECT c.name, COUNT(o.id) AS orders_count
-- FROM customers c
-- JOIN orders o ON o.customer_id = c.id
-- JOIN order_items oi ON oi.order_id = o.id
-- GROUP BY c.name;

-- (б) ИСПРАВЛЕНО: order_items тут вообще не нужен, считаем
-- различные id заказов через LEFT JOIN (чтобы не терять клиентов
-- без заказов):
SELECT c.name, COUNT(DISTINCT o.id) AS orders_count
FROM customers c
LEFT JOIN orders o ON o.customer_id = c.id
GROUP BY c.name
ORDER BY c.name;


-- ------------------------------------------------------------
-- 1.3 «Все клиенты и число их заказов за апрель 2025»
-- ------------------------------------------------------------
-- (а) ОШИБКА:
-- Условие по дате стоит в WHERE, а не в ON. LEFT JOIN сначала
-- честно подставляет NULL для клиентов без заказов вообще (или
-- без апрельских заказов), но затем WHERE проверяет
-- o.order_date >= ... — а NULL >= '2025-04-01' даёт NULL/false,
-- и такая строка выбрасывается из результата. По факту LEFT JOIN
-- превращается в INNER JOIN, и клиенты без апрельских заказов
-- (Попова Мария, Смирнов Игорь, Волкова Дарья) вообще пропадают
-- из отчёта, хотя должны быть в нём с 0.
--
-- Было (сломано):
-- SELECT c.name, COUNT(o.id) AS april_orders
-- FROM customers c
-- LEFT JOIN orders o ON o.customer_id = c.id
-- WHERE o.order_date >= '2025-04-01' AND o.order_date < '2025-05-01'
-- GROUP BY c.name;

-- (б) ИСПРАВЛЕНО: условие по дате переносим в ON, чтобы оно
-- влияло только на то, что джойнится, а не на то, что уже
-- отобрано LEFT JOIN'ом:
SELECT c.name, COUNT(o.id) AS april_orders
FROM customers c
LEFT JOIN orders o
    ON o.customer_id = c.id
   AND o.order_date >= '2025-04-01'
   AND o.order_date <  '2025-05-01'
GROUP BY c.name
ORDER BY c.name;


-- ------------------------------------------------------------
-- 1.4 «Средний чек (средняя сумма заказа) по доставленным заказам»
-- ------------------------------------------------------------
-- (а) ОШИБКА:
-- AVG(oi.qty * oi.price) считает средневзвешенное по СТРОКАМ
-- order_items (по позициям в заказах), а не по заказам целиком.
-- Если в одном заказе 1 позиция, а в другом — 5, то заказ с
-- 5 позициями "весит" в 5 раз больше при усреднении, что не
-- является средним чеком. Средний чек — это среднее ПО ЗАКАЗАМ
-- от суммы всех позиций внутри заказа. На наших данных
-- сломанный вариант даёт 2900, а правильный — 4640 (реальные
-- суммы заказов 1,2,7,8: 3900, 15000, 2400, 2400 -> avg 4640).
--
-- Было (сломано):
-- SELECT AVG(oi.qty * oi.price) AS avg_order_value
-- FROM orders o
-- JOIN order_items oi ON oi.order_id = o.id
-- WHERE o.status = 'shipped';

-- (б) ИСПРАВЛЕНО: сначала агрегируем сумму по каждому заказу,
-- потом усредняем:
SELECT AVG(order_total) AS avg_order_value
FROM (
    SELECT o.id, SUM(oi.qty * oi.price) AS order_total
    FROM orders o
    JOIN order_items oi ON oi.order_id = o.id
    WHERE o.status = 'shipped'
    GROUP BY o.id
) t;


