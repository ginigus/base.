-- ЧАСТЬ 2. Аналитика
-- ============================================================

-- ------------------------------------------------------------
-- 2.1 Топ-2 товара по выручке внутри каждой категории
-- (отменённые заказы не учитываем)
-- ------------------------------------------------------------
-- Допущение: выручка = SUM(qty * price) "как есть" из order_items,
-- включая аномальные строки (qty=0, отрицательная price) —
-- эти аномалии разбираются отдельно в части 3, здесь их не чиним,
-- чтобы честно показать, как плохие данные искажают отчёт.
-- Используем RANK() (не ROW_NUMBER), чтобы при равной выручке оба
-- товара попали в топ-2.
SELECT category, title, revenue, rnk
FROM (
    SELECT
        p.category,
        p.title,
        SUM(oi.qty * oi.price) AS revenue,
        RANK() OVER (
            PARTITION BY p.category
            ORDER BY SUM(oi.qty * oi.price) DESC
        ) AS rnk
    FROM order_items oi
    JOIN products p ON p.id = oi.product_id
    JOIN orders   o ON o.id = oi.order_id
    WHERE o.status <> 'cancelled'
    GROUP BY p.category, p.title
) t
WHERE rnk <= 2
ORDER BY category, rnk;


-- ------------------------------------------------------------
-- 2.2 Выручка по месяцам и прирост к предыдущему месяцу, %
-- ------------------------------------------------------------
-- Допущение: отменённые заказы также исключены из выручки
-- (как и в 2.1) — считаем, что "выручка" должна отражать реально
-- состоявшиеся продажи. Формат месяца: 'YYYY-MM' (strftime — функция
-- SQLite; в PostgreSQL — to_char(order_date, 'YYYY-MM'),
-- в MySQL — DATE_FORMAT(order_date, '%Y-%m')).
SELECT
    month,
    revenue,
    ROUND(
        (revenue - LAG(revenue) OVER (ORDER BY month)) * 100.0
        / LAG(revenue) OVER (ORDER BY month),
    2) AS growth_pct
FROM (
    SELECT
        strftime('%Y-%m', o.order_date) AS month,
        SUM(oi.qty * oi.price) AS revenue
    FROM orders o
    JOIN order_items oi ON oi.order_id = o.id
    WHERE o.status <> 'cancelled'
    GROUP BY month
) t
ORDER BY month;


-- ------------------------------------------------------------
-- 2.3 Первый и второй заказ каждого зарегистрированного клиента,
-- дней между ними (NULL, если заказ один или их нет)
-- ------------------------------------------------------------
-- Берём только клиентов из customers (в т.ч. без заказов вообще —
-- LEFT JOIN), гостевые заказы (customer_id IS NULL) не участвуют,
-- так как вопрос про "зарегистрированного клиента".
-- ROW_NUMBER разбивает заказы клиента по порядку даты; id заказа
-- как tie-breaker на случай двух заказов в один день.
WITH ranked AS (
    SELECT
        o.customer_id,
        o.order_date,
        o.id,
        ROW_NUMBER() OVER (
            PARTITION BY o.customer_id
            ORDER BY o.order_date, o.id
        ) AS rn
    FROM orders o
    WHERE o.customer_id IS NOT NULL
)
SELECT
    c.name,
    r1.order_date AS first_order,
    r2.order_date AS second_order,
    CASE WHEN r2.order_date IS NOT NULL
         THEN julianday(r2.order_date) - julianday(r1.order_date)
    END AS days_between
FROM customers c
LEFT JOIN ranked r1 ON r1.customer_id = c.id AND r1.rn = 1
LEFT JOIN ranked r2 ON r2.customer_id = c.id AND r2.rn = 2
ORDER BY c.name;
-- В PostgreSQL julianday(...)-julianday(...) заменить на
-- (r2.order_date - r1.order_date) — там разница дат уже целое число дней.


-- ------------------------------------------------------------
-- 2.4 Доля выручки от гостевых заказов, %
-- ------------------------------------------------------------
-- Допущение: отменённые заказы не учитываем (аналогично 2.1/2.2).
SELECT
    ROUND(
        SUM(CASE WHEN o.customer_id IS NULL THEN oi.qty * oi.price ELSE 0 END) * 100.0
        / SUM(oi.qty * oi.price),
    2) AS guest_revenue_pct
FROM orders o
JOIN order_items oi ON oi.order_id = o.id
WHERE o.status <> 'cancelled';


