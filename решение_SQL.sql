-- ============================================================
-- ЧАСТЬ 4. Проектирование: история цен
-- ============================================================

-- ------------------------------------------------------------
-- 4.1 DDL новой таблицы + пояснение
-- ------------------------------------------------------------
-- Идея: полуоткрытый интервал действия цены [valid_from, valid_to).
-- valid_to = NULL означает "цена действует по настоящий момент".
-- UNIQUE(product_id, valid_from) не даёт завести два периода с
-- одинаковым началом для одного товара (защита от дублей).
-- CHECK(price >= 0) — та же защита, что и в order_items (часть 3).
CREATE TABLE product_prices (
    id         INTEGER PRIMARY KEY,
    product_id INT NOT NULL REFERENCES products(id),
    price      DECIMAL(10,2) NOT NULL CHECK (price >= 0),
    valid_from DATE NOT NULL,
    valid_to   DATE,              -- NULL = текущая действующая цена
    UNIQUE (product_id, valid_from)
);

-- Тестовые данные для товара "Клавиатура" (id=1):
-- 3 периода: с 01.01 по 01.03 — 2300, с 01.03 по 01.05 — 2500,
-- с 01.05 и по настоящее время — 2700.
INSERT INTO product_prices (product_id, price, valid_from, valid_to) VALUES
(1, 2300.00, '2025-01-01', '2025-03-01'),
(1, 2500.00, '2025-03-01', '2025-05-01'),
(1, 2700.00, '2025-05-01', NULL);

-- Почему так, а не "просто добавить колонку effective_date
-- в products": потому что тогда мы теряем историю (products —
-- это "текущее состояние", а не журнал). Отдельная таблица —
-- классический паттерн "slowly changing dimension" / "temporal table".
-- Изменение цены = INSERT новой строки + UPDATE предыдущей
-- (проставить ей valid_to = дата начала новой цены), а не UPDATE
-- существующей цены "поверх" — это и обеспечивает требование
-- "отчёты за прошлые периоды не меняются задним числом".

-- ------------------------------------------------------------
-- 4.2 Запрос: цена "Клавиатуры" на 2025-04-15
-- ------------------------------------------------------------
SELECT price
FROM product_prices
WHERE product_id = (SELECT id FROM products WHERE title = 'Клавиатура')
  AND valid_from <= '2025-04-15'
  AND (valid_to IS NULL OR valid_to > '2025-04-15');
-- Ожидаемый результат: 2500.00 (проверено на тестовых данных выше)


-- ------------------------------------------------------------
-- 4.3 Нужна ли колонка products.price после введения истории?
-- ------------------------------------------------------------
-- Аргументы ЗА то, чтобы оставить products.price:
--   - быстрый доступ к "текущей" цене без обращения к
--     product_prices (листинг каталога, поиск, витрина);
--   - не нужно писать WHERE valid_to IS NULL в каждом запросе
--     каталога.
-- Аргументы ПРОТИВ:
--   - дублирование источника истины: если забыть обновить
--     products.price при добавлении новой строки в
--     product_prices, данные разойдутся;
--   - лишняя точка отказа/рассинхронизации, которую надо
--     поддерживать в актуальном состоянии (транзакционно).
--
-- Решение: убрать products.price. Единственный источник истины —
-- product_prices. "Текущую" цену получать как строку с
-- valid_to IS NULL (или MAX(valid_from) без valid_to), и для
-- производительности сделать частичный индекс:
--   CREATE INDEX idx_current_price ON product_prices(product_id)
--   WHERE valid_to IS NULL;
-- (частичные индексы поддерживаются в PostgreSQL/SQLite; в MySQL
-- аналога нет — там просто обычный индекс на (product_id, valid_to)).
-- Если нужен супер-быстрый "текущая цена без единого JOIN",
-- альтернатива — материализованное представление
-- (VIEW/MATERIALIZED VIEW), а не отдельная "живая" колонка в products.


-- ============================================================
-- ЧАСТЬ 5. Производительность (orders — 10 млн, order_items — 40 млн)
-- ============================================================

-- ------------------------------------------------------------
-- 5.1 Индексы для запроса 2.3
-- ------------------------------------------------------------
-- Запрос 2.3 в CTE делает:
--   WHERE o.customer_id IS NOT NULL
--   PARTITION BY o.customer_id ORDER BY o.order_date, o.id
-- Составной индекс, покрывающий и фильтр, и партиционирование,
-- и сортировку внутри партиции — экономит и поиск, и сортировку
-- (планировщик сможет читать уже отсортированные по order_date
-- строки для каждого customer_id, не делая отдельный SORT):
CREATE INDEX idx_orders_customer_date_id
    ON orders (customer_id, order_date, id);
-- customer_id первый — по нему идёт равенство/партиционирование
-- и фильтрация (высокая избирательность, много клиентов).
-- order_date вторым — порядок внутри партиции нужен для
-- ROW_NUMBER() OVER (... ORDER BY order_date).
-- id третьим — как tie-breaker, тоже входит в ORDER BY.
--
-- order_items в этом конкретном запросе не участвует, доп. индекс
-- туда для 2.3 не нужен. Если бы участвовал JOIN по order_id —
-- существующий PRIMARY KEY (order_id, product_id) уже покрывает
-- поиск по order_id как по первой колонке композитного PK.

-- ------------------------------------------------------------
-- 5.2 Как проверить, что индекс реально помог (не только секундомером)
-- ------------------------------------------------------------
-- - EXPLAIN QUERY PLAN <запрос>; в SQLite / EXPLAIN (ANALYZE, BUFFERS)
--   в PostgreSQL / EXPLAIN ANALYZE в MySQL — смотрим, что план
--   стал использовать Index Scan / Index Only Scan вместо
--   Seq Scan (Full Table Scan), и что убрался отдельный шаг
--   "Sort" (раз сортировка уже "бесплатна" благодаря индексу).
-- - Сравнить количество прочитанных страниц/буферов
--   (BUFFERS в Postgres) до и после — должно резко упасть.
-- - Смотреть на actual rows vs estimated rows в плане — если
--   планировщик "промахивается" по оценке, возможно нужна
--   актуализация статистики (ANALYZE / OPTIMIZE TABLE).
-- - Мониторинг в проде: pg_stat_user_indexes (idx_scan — растёт
--   ли использование индекса), количество logical reads в MySQL
--   Performance Schema.
-- - Нагрузочное A/B на одинаковых данных/железе, а не разовый замер.

-- ------------------------------------------------------------
-- 5.3 Ответ на "давайте проиндексируем каждую колонку в каждой таблице"
-- ------------------------------------------------------------
-- Так делать не стоит:
--   1. Каждый индекс замедляет INSERT/UPDATE/DELETE — он должен
--      поддерживаться в актуальном состоянии при каждой записи.
--      При 10-40 млн строк и активной записи это может быть
--      дороже, чем выигрыш на чтении.
--   2. Индексы занимают дополнительное место на диске (иногда
--      сравнимое с самой таблицей или больше).
--   3. Индекс на колонку с низкой селективностью (например,
--      status с 4 значениями на 10 млн строк) почти бесполезен —
--      планировщик скорее всего его проигнорирует и пойдёт
--      Seq Scan'ом, т.к. читать через индекс большую долю таблицы
--      дороже, чем просто прочитать её целиком.
--   4. Индекс нужно строить под конкретный паттерн запроса
--      (что в WHERE/JOIN/ORDER BY, и в каком порядке колонки) —
--      "по одной колонке на индекс" не поможет составным условиям
--      так, как это делает один правильный композитный индекс
--      (как в 5.1).
--   5. Слишком много индексов может сбивать оптимизатор с толку
--      при выборе плана (больше вариантов — не всегда лучше).
-- Предложение: строить индексы осознанно, под реальные (или
-- ожидаемые) запросы, на основе EXPLAIN/логов медленных запросов,
-- и проверять эффект каждого индекса до/после, а не "на всякий случай".
